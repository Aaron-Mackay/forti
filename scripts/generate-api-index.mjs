import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const apiRoot = path.join(repoRoot, 'src', 'app', 'api');
const outputPath = path.join(repoRoot, 'docs', 'agent', 'api-index.md');

const HTTP_METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'OPTIONS', 'HEAD'];

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(entries.map(async (entry) => {
    const absolute = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      return walk(absolute);
    }
    return absolute.endsWith('route.ts') ? [absolute] : [];
  }));

  return files.flat();
}

function toRoutePath(routeFilePath) {
  const routeDir = path.dirname(routeFilePath);
  const relativeRouteDir = path.relative(apiRoot, routeDir).split(path.sep).join('/');
  return relativeRouteDir.length > 0 ? `/api/${relativeRouteDir}` : '/api';
}

function detectMethods(content) {
  const methods = HTTP_METHODS.filter((method) => (
    new RegExp(`export\\s+async\\s+function\\s+${method}\\b|export\\s+function\\s+${method}\\b`).test(content)
    || new RegExp(`export\\s+const\\s+${method}\\b`).test(content)
    || new RegExp(`\\bas\\s+${method}\\b`).test(content)
  ));

  return methods.length > 0 ? methods.join(',') : 'UNKNOWN';
}

function detectAuthRequirement(content) {
  if (/\brequireSession\s*\(/.test(content) || /\bgetLoggedInUser\s*\(/.test(content)) {
    return 'session';
  }

  if (/\bCRON_SECRET\b/.test(content) || /\bx-cron-secret\b/i.test(content) || /\bauthorization\b/i.test(content)) {
    return 'secret/header';
  }

  return 'none';
}

function detectContractModulePath(content) {
  const importRegex = /from\s+['"]([^'"]*lib\/contracts\/[^'"]+)['"]/g;
  const modules = new Set();

  let match;
  while ((match = importRegex.exec(content)) !== null) {
    modules.add(match[1]);
  }

  if (modules.size === 0) {
    return '-';
  }

  return Array.from(modules).sort().join(',');
}

async function getEndpointMeta(routeFilePath) {
  const content = await fs.readFile(routeFilePath, 'utf8');

  return {
    routePath: toRoutePath(routeFilePath),
    methods: detectMethods(content),
    authRequirement: detectAuthRequirement(content),
    contractModulePath: detectContractModulePath(content),
  };
}

async function main() {
  const routeFiles = await walk(apiRoot);
  const endpoints = (await Promise.all(routeFiles.map(getEndpointMeta)))
    .sort((a, b) => a.routePath.localeCompare(b.routePath));

  const lines = [
    '# API Index',
    '',
    '> Auto-generated by `npm run api:index`. Do not edit by hand.',
    '',
    ...endpoints.map((endpoint) =>
      `- ${endpoint.routePath} | methods=${endpoint.methods} | auth=${endpoint.authRequirement} | contracts=${endpoint.contractModulePath}`,
    ),
    '',
  ];

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, `${lines.join('\n')}`, 'utf8');
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
